package org.compass.core.test.dynamicproperty.map.compound.array2;

/**
 * @author kimchy
 */
public class DynaValue {

    String[] value1;

    String[] value2;

    public DynaValue(String[] value1, String[] value2) {
        this.value1 = value1;
        this.value2 = value2;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        DynaValue dynaValue = (DynaValue) o;

        if (value1 != null ? !value1.equals(dynaValue.value1) : dynaValue.value1 != null) return false;
        if (value2 != null ? !value2.equals(dynaValue.value2) : dynaValue.value2 != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = value1 != null ? value1.hashCode() : 0;
        result = 31 * result + (value2 != null ? value2.hashCode() : 0);
        return result;
    }
}